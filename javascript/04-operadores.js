//OPERADORES
console.log("********OPERADORES********");
console.log(5 == 5); // true
console.log(9 === 9);   // igualdad estricta
console.log(9 != 6);  // desigualdad
console.log(1 !== "1"); // desigualdad estricta
console.log(6 > 5);  // desigualdad
console.log(5 >= 5); // Mayor o igual que: >=
console.log(5 < 6);  // Menor que: <
console.log(5 <= 5); // Menor o igual que: <=

//OPERADORES LOGICOS
console.log("********OPERADORES LOGICOS********");
console.log(true && true); // AND
console.log(true || true); //OR
console.log(!true); //NOT


